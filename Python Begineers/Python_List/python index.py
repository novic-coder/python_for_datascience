# To select items inside a list we use index

#  indexing always starts with o --- called oth index

lis = ['Ravi', 1.56, 'Nikita', 1.53, 'mom', 1.50, 'dad', 1.50]

# getting height of nikita
print(lis[3])

# getting dad string

print(lis[6])


# negative indexing is used to access the element from the last

print(lis[-1])


# slicing -- allows to select multiple element

print(lis[3:5])

print(lis[1:4])

print(lis[:4])